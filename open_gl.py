def Plane(struct):
    if struct:
        glBegin(GL_LINES)
        for edge in plane_edges:
            for vertex in edge:
                glVertex3fv(plane_verticies_central[vertex])
        glEnd()
    else:
        i = 0
        glBegin(GL_QUADS)
        while i < len(plane_verticies_central):
            tex = texCoords[i]
            vert = plane_verticies_central[i]
            glTexCoord2f(tex[0], tex[1])
            glVertex3f(vert[0], vert[1], vert[2])
            i+=1
        glEnd()

