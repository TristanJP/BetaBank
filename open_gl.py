    display = (800,600)
    pygame.display.set_mode(display, DOUBLEBUF|OPENGL)

    loadTexture()

    gluPerspective(45, (display[0]/display[1]), 0.1, 90.0)

    glTranslatef(0.0,0.0, -5)

    while True:
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                pygame.quit()
